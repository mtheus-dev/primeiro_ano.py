def soma(numero1,numero2):
    resultado = numero1 + numero2
    return resultado

def subtracao(numero1,numero2):
    resultado = numero1 - numero2
    return resultado

def divisao(numero1,numero2):
    resultado = numero1 / numero2
    return resultado

def multilicacao(numero1,numero2):
    resultado = numero1 * numero2
    return resultado

def modulo(numero1,numero2):
    resultado = numero1 % numero2
    return resultado